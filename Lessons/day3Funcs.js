//Functions

//1. Declarations 
function myDeclaration (num1, num2) {
    return num1 * num2;
}
console.log(myDeclaration(5, 5));

//2. Arrow Funcs
const myArrow = (num3, num4) => {
    return num3 * num4;
} 
console.log(myArrow(5, 5))

//3. Anonymous/Expression Funcs
const myAnonymous = function (num5, num6) {
    return num5 * num6;
}
console.log(myAnonymous(5, 5))



